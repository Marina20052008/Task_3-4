def move(x1, y1, x2, y2):
    # Ферзь рухається, якщо клітинки на одній діагоналі.
    return x1 == x2 or y1 == y2 or abs(x1 - x2) == abs(y1 - y2)

x1 = int(input("Координата x для першої клітини: "))
y1 = int(input("Координата y для першої клітини: "))
x2 = int(input("Координата x для другої клітини: "))
y2 = int(input("Координата y для другої клітини: "))
# Проводимо перевірку руху.
if move(x1, y1, x2, y2):
    print("YES")
else:
    print("NO")
